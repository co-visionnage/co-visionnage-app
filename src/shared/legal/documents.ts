export function getLegalDocument(slug: string) {
  return LEGAL_DOCUMENTS[slug as LegalDocument['slug']] ?? undefined;
}

export function legalDocumentToPlainText(document: LegalDocument) {
  return [
    document.title,
    `Редакция от ${document.updatedAt}`,
    '',
    ...document.sections.flatMap((section) => [
      section.title,
      section.text,
      '',
    ]),
  ].join('\n');
}
